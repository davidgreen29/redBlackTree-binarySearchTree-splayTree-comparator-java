package tree.splay;
//node class for splay tree 
public class splayNode {
	
	int data; 
	splayNode parent; 
	splayNode left; 
	splayNode right; 
//constructors for the splayNode class
	public splayNode(int data) {
		this.data = data;
		this.parent = null;
		this.left = null;
		this.right = null;
	}
}
	 

	 
